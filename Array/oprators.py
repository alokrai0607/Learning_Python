#Arthmetic Oprators
a = 10
b = 5

print(a+b)   #this is for addition
print(a-b)   #this is for substraction
print(a*b)   #this is for multiplication
print(a/b)   #this is for divisor
print(a%b)   # this is for remainder
print(a**b)  # this is for square


#Relational OR Comparison Oprator (== , != , > , < , >= , <=)

a = 10
b = 20

print(a==b)  #false
print(a!=b)  #true
print(a>b)  #false
print(a<b)  #true
print(a>=b) #false
print(a<=b) #true


