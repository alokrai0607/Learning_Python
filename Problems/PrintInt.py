def printInt(n):
    if (n>0) :
        print(f"{n}"" : is Positive Integer")
    elif (n==0) :
        print(f"{n}"" : is Zero Integer")
    elif (n<0) :
        print(f"{n}"" : is Negative Integer")

print(printInt(-1))
print(printInt(0))
print(printInt(1))
print(printInt(-11))
printInt(55)
printInt(100)

